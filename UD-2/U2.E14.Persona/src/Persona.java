public class Persona {
    
    public String nombre;
    public int edad;

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setNombre(String nuevoNombre) {
        nombre = nuevoNombre;
    }

    public void setEdad(int nuevaEdad) {
        edad = nuevaEdad;
    }
    
    
    
}
