public class PruebaTablaMulti9 {
    public static void main(String[] args) {
        
        final int TABLA9=9;
        int multi=0;
        
        while(multi<=10){
            System.out.println("9x"+multi+"= "+TABLA9*multi);
            multi++;
        }
        
    }
}
